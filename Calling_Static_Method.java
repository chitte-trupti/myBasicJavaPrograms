package javaprograms.assignments;

public class Calling_Static_Method 
{
    
    
    static void A1()         //Static method declaration
    {
    	
    	System.out.println("Trupti");
    	
    }
    
    static void A2()
    {
    	System.out.println("Amey");
   
    }
    
    static void A3()
    {
    	System.out.println("Shivani");
    }
	public static void main(String[] args) 
	{
		
		A1();         //Calling static method inside the main method
		A2();
		A3();

	}

}
